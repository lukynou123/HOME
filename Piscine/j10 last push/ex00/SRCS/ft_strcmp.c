int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int stock;

	i = 0;
	if (s1[i] == '\0' && s2[i] == '\0')
		return (0);
	while (s1[i] == s2[i])
	{
		i++;
		if (s1[i] == '\0' && s2[i] == '\0')
			return (0);
	}
	stock = (s1[i] - s2[i]);
	return (stock);
}
