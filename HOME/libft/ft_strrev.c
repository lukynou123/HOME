char	*ft_strrev(char *str)
{
	char	c;
	int		i;
	int		b;

	b = 0;
	i = 0;
	while (str[i] != '\0')
		i++;
	i--;
	while (i > b)
	{
		c = str[b];
		str[b] = str[i];
		str[i] = c;
		b++;
		i--;
	}
	return (str);
}
